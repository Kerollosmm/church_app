/// Domain entity representing an attendance session.
///
/// A session represents a scheduled class meeting where attendance is taken.
///
/// Fields:
/// - id: Unique session identifier.
/// - classId: Reference to the class/grade.
/// - date: Date of the session.
/// - startTime: Scheduled start time.
/// - endTime: Scheduled end time.
/// - servantId: Servant responsible for this session.
/// - status: Session status (scheduled, in_progress, completed).
/// - createdAt: When the session was created.
